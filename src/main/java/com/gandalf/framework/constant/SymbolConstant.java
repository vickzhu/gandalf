package com.gandalf.framework.constant;

/**
 * 类SymbolConstant.java的实现描述：符号常量
 * 
 * @author gandalf 2014-2-20 下午4:09:08
 */
public class SymbolConstant {

    /**
     * 逗号[,]
     */
    public static final String COMMA        = ",";

    /**
     * 点号[.]
     */
    public static final String DOT          = ".";

    /**
     * 斜杠[/]
     */
    public static final String SLASH        = "/";

    /**
     * 双斜杠[//]
     */
    public static final String DOUBLE_SLASH = "//";

    /**
     * 反斜杠[\]
     */
    public static final String BACK_SLASH   = "\\";

    /**
     * 竖线[|]
     */
    public static final String V_LINE       = "|";

    /**
     * 横线[-]
     */
    public static final String H_LINE       = "-";

    /**
     * 下划线[_]
     */
    public static final String U_LINE       = "_";

    /**
     * 冒号[:]
     */
    public static final String COLON        = ":";
    /**
     * 问号[?]
     */
    public static final String QUESTION     = "?";
    /**
     * 和[&]
     */
    public static final String AND          = "&";
    /**
     * 等号[=]
     */
    public static final String EQUALS       = "=";
    /**
     * 双引号
     */
    public static final String DOUBLE_QUOTE = "\"";
    /**
     * 单引号
     */
    public static final String SINGLE_QUOTE = "'";

}
